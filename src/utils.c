#include "utils.h"

uint32_t	atoio(char **it)
{
	int64_t	i;

	i = 0;
	while ((**it >= '0' && **it <= '9') && i >= 0)
		i = i * 10 + *(*it)++ - '0';
	if (i < 0 || i > UINT32_MAX)
	{
		while (**it >= '0' && **it <= '9')
			++*it;
		i = 0;
	}
	return ((uint32_t)i);
}
